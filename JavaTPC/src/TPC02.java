
public class TPC02 {

	public static void main(String[] args) {
		
		// 프로그래밍의 3대 요소: 변수, 자료형(DataType), 할당(=)
		// 1+1=2
		int a,b,sum;
		a=1;
		b=1;
		sum=a+b;
		System.out.println(sum);
		
		float f; // float, double
		f=34.5f;
		System.out.println(f);
		
		char c;
		c = 'C';
		System.out.println(c);
		
		boolean t;
		t=true; // true(참), false(거짓)
		System.out.println(t);
	}

}
